package common;

/**
 * An interface to be implemented by any exercise demonstration. Contains a single method to be called
 * to trigger execution.
 * 
 * @author Aditya Srinivasan
 *
 */
public interface IExerciseDemo {
	
	/**
	 * Runs the demonstration, as specified by the class implementing the interface.
	 */
	public void executeDemo();

}
